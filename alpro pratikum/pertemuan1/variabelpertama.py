#variable angka1 dan angka2 untuk menginput angka yang ingin dioperasikan
angka1 = int(input("masukan angka pertama: "))
angka2 = int(input("masukan angka kedua: "))

#variable operasi
tambah = angka1 + angka2
kurang = angka1 - angka2
bagi = angka1 / angka2

#output hasil
print(f"{angka1} ditambah {angka2} = {tambah}")
print(f"{angka1} dikurangi {angka2} = {kurang}")
print(f"{angka1} dibagi {angka2} = {bagi}")

