#operator >
print('apakah benar 5>4 ?' ,5>4)
print('apakah benar 5>5 ?' ,5>5)

#operator <
print('apakah benar 6<3 ?' ,6<3)
print('apakah benar 6<6 ?' ,6<6)

#operator >=
print('apakah benar 7>=3 ?' ,7>=3)
print('apakah benar 7>=5 ?' ,7>=5)

#operator <= (listen or equaltu)
print('apakah benar 8<=3?' ,8<=3)
print('apakah benar 8<=9 ?',8<=9)

#operator == (equaltu)
print('apakah benar 9==9 ?',9==5)
print('apakah benar 9==8 ?',9==9)

#operator != (not equaltu tidak sama dengan)
print('apakah benar 10!=10 ?',10!=10)
print('apakah benar 10!=11 ?',10!=11)

